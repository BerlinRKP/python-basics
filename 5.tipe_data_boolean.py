# TIPE DATA BOOLEAN
print("===============TIPE DATA BOOLEAN===============")
# Tipe data boolean hanya bisa diisi dengan salah satu dari dua nilai yaitu True dan False. tipe data ini biasanya dipakai untuk percabangan kode program atau untuk memutuskan apa yang mesti dijalankan ketika sebuah kondisi terjadi.

benar = True
salah = False

print(benar, salah)

a = 1 < 2
b = 1 > 2
c = 1 == 2

print("1 < 2 = ", a)
print("1 > 2 = ", b)
print("1 == 2 = ", c)

